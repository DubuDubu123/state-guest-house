  $encrypted = openssl_encrypt($data, 'aes-256-cbc', $key, OPENSSL_RAW_DATA, $iv);

  // Combine IV and encrypted data
  $encryptedWithIV = base64_encode($iv . $encrypted);

  return $encryptedWithIV;
}

