        function calculateDateOfBirth(dobString) {
    // Parse the input date string in the format 'dd-mm-yyyy'
    const [year, month, day] = dobString.split('-').map(Number);

    // Create a new Date object from the parsed values
    let dob = new Date(year, month - 1, day);

    // Add 60 years to the date
    dob.setFullYear(dob.getFullYear() + 60);

    // If the day is 1, adjust the month and day according to the formula
    if (day === 1) {
        dob.setMonth(dob.getMonth() - 1);
        dob.setDate(30);
    } else {
        // If the day is greater than 1, adjust the day according to the month
        const longMonths = [0, 2, 4, 6, 7, 9, 11]; // months with 31 days (0-based index)
        if (longMonths.includes(dob.getMonth())) {
            dob.setDate(31);
        } else {
            dob.setDate(30);
        }
    }

      // Format the date back to 'dd-mm-yyyy'
      const formattedDay = String(dob.getDate()).padStart(2, '0');
    const formattedMonth = String(dob.getMonth() + 1).padStart(2, '0'); // Months are zero-based
    const formattedYear = dob.getFullYear();
